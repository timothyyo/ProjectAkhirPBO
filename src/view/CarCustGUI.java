    private void BtCariMobilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtCariMobilActionPerformed
        // Ambil nilai filter dari ComboBox
        String selectedFilter = (String) CbSortingListMobil.getSelectedItem();
        // Ambil keyword dari TextField
        String keyword = TxCariMobil.getText();

        // Cek apakah keyword kosong
        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a keyword for the search.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Lakukan pencarian dan mendapatkan hasil
        String hasilPencarian = rentalService.searchCar(selectedFilter, keyword);

        // Set model tabel dengan hasil pencarian
        DefaultTableModel model = new DefaultTableModel(new Object[]{"ID", "Car Name", "Capacity", "Brand", "Price", "Status"}, 0);

        if (!hasilPencarian.equals("No vehicles found with the given criteria.")) {
            String[] results = hasilPencarian.split("\n\n");
            for (String result : results) {
                String[] details = result.split("\n");
                if (details.length >= 6) { // Perhatikan 6 kolom, termasuk ID
                    String id = details[0].replace("ID: ", "");
                    String carName = details[1].replace("Car Name: ", "");
                    String capacity = details[2].replace("Capacity: ", "");
                    String brand = details[3].replace("Brand: ", "");
                    String price = details[4].replace("Price: ", "");
                    String status = details[5].replace("Status: ", "");
                    model.addRow(new Object[]{id, carName, capacity, brand, price, status});
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, hasilPencarian, "Search Result", JOptionPane.INFORMATION_MESSAGE);
        }

        // Set model ke jTable1 untuk menampilkan hasil pencarian
        jTable1.setModel(model);
    }//GEN-LAST:event_BtCariMobilActionPerformed

