    private void loadCarList() {
        // Tambahkan kolom "ID" pada tabel
        DefaultTableModel model = new DefaultTableModel(new Object[]{"ID", "Car Name", "Capacity", "Brand", "Rental Price", "Status"}, 0);

        // Ambil daftar kendaraan
        List<Kendaraan> daftarKendaraan = rentalService.getDaftarKendaraan();
        for (Kendaraan kendaraan : daftarKendaraan) {
            // Tambahkan data kendaraan ke model tabel
            model.addRow(new Object[]{
                kendaraan.getId(),         // ID kendaraan
                kendaraan.getNamaMobil(),       // Nama kendaraan
                kendaraan.getKapasitas(),  // Kapasitas
                kendaraan.getMerek(),      // Merek kendaraan
                kendaraan.getHarga(),      // Harga sewa
                kendaraan.getStatus()      // Status
            });
        }

        // Set model tabel
        jTable1.setModel(model);
    }

