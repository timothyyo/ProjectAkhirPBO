    private void loadCarIDs() {
        // Clear existing items
        Cbidmobil.removeAllItems();
        // Fetch available cars
        for (Kendaraan kendaraan : rentalService.getAvailableCars()) {
            if (kendaraan != null) { // Tambahkan validasi
                Cbidmobil.addItem(kendaraan.getId() + " - " + kendaraan.getNamaMobil());
            }
        }
    }

    private void populateCarDetails() {
        String selectedCar = (String) Cbidmobil.getSelectedItem();
        if (selectedCar != null && !selectedCar.isEmpty()) {
            try {
                int carId = Integer.parseInt(selectedCar.split(" - ")[0]);
                Kendaraan kendaraan = rentalService.getCarDetails(carId);
                if (kendaraan != null) { // Validasi jika kendaraan tidak null
                    TxCarName.setText(kendaraan.getNamaMobil());
                    TxBrand.setText(kendaraan.getMerek());
                    TxCapacity.setText(kendaraan.getKapasitas());
                    TxPricepHour.setText(String.valueOf(kendaraan.getHarga()));
                } else {
                    JOptionPane.showMessageDialog(this, "Car details not found!");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid car ID format.");
            }
        }
    }
    
    private void calculateTotalPrice() {
        try {
            int pricePerDay = Integer.parseInt(TxPricepHour.getText());
            String selectedDuration = (String) CbHour.getSelectedItem();
            int days = 0;

            switch (selectedDuration) {
                case "1 Days":
                    days = 1;
                    break;
                case "3 Days":
                    days = 3;
                    break;
                case "7 Days":
                    days = 7;
                    break;
                case "14 Days":
                    days = 14;
                    break;
                case "30 Days":
                    days = 30;
                    break;
            }

            int totalPrice = pricePerDay * days;
            TxTotalPrice.setText(String.valueOf(totalPrice));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid price format.");
        }
    }
    
