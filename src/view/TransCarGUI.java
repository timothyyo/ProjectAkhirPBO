    private void BtConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtConfirmActionPerformed
        try {
            String selectedCar = (String) Cbidmobil.getSelectedItem();
            int carId = Integer.parseInt(selectedCar.split(" - ")[0]);
            String selectedDuration = (String) CbHour.getSelectedItem();
            int totalPrice = Integer.parseInt(TxTotalPrice.getText());

            // Call RentalService to create a rental transaction
            boolean success = rentalService.rentCar(carId, selectedDuration, totalPrice);
            if (success) {
                JOptionPane.showMessageDialog(this, "Car rental confirmed!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to confirm rental.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please fill in all the fields correctly.");
        }
    }//GEN-LAST:event_BtConfirmActionPerformed

