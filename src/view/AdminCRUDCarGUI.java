    private void loadCarList() {
        DefaultTableModel model = new DefaultTableModel();

        // Menambahkan kolom pada tabel
        model.addColumn("ID");
        model.addColumn("Car Name");
        model.addColumn("Capacity");
        model.addColumn("Brand");
        model.addColumn("Rental Price");
        model.addColumn("Status");

        // Mengambil data kendaraan langsung dari database
        try {
            // Membuka koneksi ke database
            Connection conn = db_rentalKendaraan.getConnection();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM mobil");

            // Mengambil data dari ResultSet dan menambahkannya ke model tabel
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nama_mobil"),
                    rs.getString("kapasitas"),
                    rs.getString("merek"),
                    rs.getString("harga_sewa"),
                    rs.getString("status")
                });
            }

            // Menetapkan model pada jTable1
            jTable1.setModel(model);

            // Menutup koneksi setelah selesai
            conn.close();
        } catch (Exception e) {
            // Menampilkan pesan kesalahan jika gagal mengambil data
            JOptionPane.showMessageDialog(null, "Gagal memuat data!");
            e.printStackTrace();
        }
    }
    
