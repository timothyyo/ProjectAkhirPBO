    private void BtEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtEditActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int id = (int) jTable1.getValueAt(selectedRow, 0);
            String namaMobil = TxCarName.getText();
            String kapasitas = TxPricePHours1.getText();
            String merek = TxBrand.getText();
            double harga;
            try {
                harga = Double.parseDouble(TxPricePHours.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid price value.");
                return; // Exit the method if there's an error in parsing the price
            }
            String status = "Available"; // Tentukan status baru jika perlu

            Kendaraan kendaraan = new Kendaraan(id, namaMobil, kapasitas, merek, harga, status);
            rentalService.updateCar(kendaraan);
            loadCarList();
            clearText();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a car to edit.");
        }
    }//GEN-LAST:event_BtEditActionPerformed

